package io.theysay.preceive.batch.http;

import java.util.Objects;

public final class Header {
    public static final Header GZIP_ENCODING = new Header("Accept-Encoding", "gzip");
    public static final Header KEEP_ALIVE = new Header("Connection", "Keep-Alive");


    public String name;
    public String value;

    public Header(String name, String value) {
        this.name = name;
        this.value = value;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof Header)) return false;
        Header header = (Header) o;
        return Objects.equals(name, header.name) &&
                Objects.equals(value, header.value);
    }

    @Override
    public int hashCode() {
        return name.hashCode();
    }

    @Override
    public String toString() {
        return name + " : " + value;
    }
}
